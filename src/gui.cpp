#include <iostream>
#include <string>

// --- 1. 字符字典：获取指定字符的点阵数据 ---
const unsigned char* getFontData(char c) {
    // 静态定义我们需要用到的几个英文字母的点阵
    static unsigned char font_T[16] = {0x00,0x00,0x7e,0x08,0x08,0x08,0x08,0x08,0x08,0x08,0x08,0x08,0x08,0x00,0x00,0x00};
    static unsigned char font_e[16] = {0x00,0x00,0x00,0x00,0x00,0x3c,0x42,0x7e,0x40,0x40,0x40,0x42,0x3c,0x00,0x00,0x00};
    static unsigned char font_m[16] = {0x00,0x00,0x00,0x00,0x00,0x7c,0x92,0x92,0x92,0x92,0x92,0x92,0x92,0x00,0x00,0x00};
    static unsigned char font_p[16] = {0x00,0x00,0x00,0x00,0x00,0x5c,0x62,0x42,0x42,0x42,0x42,0x62,0x5c,0x40,0x40,0x40};
    static unsigned char font_C[16] = {0x00,0x00,0x3c,0x42,0x40,0x40,0x40,0x40,0x40,0x40,0x40,0x42,0x3c,0x00,0x00,0x00};
    static unsigned char font_colon[16] = {0x00,0x00,0x00,0x00,0x18,0x18,0x00,0x00,0x00,0x18,0x18,0x00,0x00,0x00,0x00,0x00};
    static unsigned char font_space[16] = {0x00}; // 空格就是全 0
    static unsigned char font_dot[16] = {0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x00,0x18,0x18,0x00,0x00,0x00,0x00};	
    // 完整的 0-9 数字字库
    static unsigned char font_nums[10][16] = {
        {0x00,0x00,0x3c,0x42,0x42,0x42,0x42,0x42,0x42,0x42,0x42,0x42,0x3c,0x00,0x00,0x00}, // 0
        {0x00,0x00,0x08,0x18,0x28,0x08,0x08,0x08,0x08,0x08,0x08,0x08,0x3e,0x00,0x00,0x00}, // 1
        {0x00,0x00,0x3c,0x42,0x42,0x02,0x02,0x0c,0x10,0x20,0x40,0x42,0x7e,0x00,0x00,0x00}, // 2
        {0x00,0x00,0x3c,0x42,0x42,0x02,0x02,0x3c,0x02,0x02,0x42,0x42,0x3c,0x00,0x00,0x00}, // 3
        {0x00,0x00,0x08,0x18,0x28,0x48,0x48,0x88,0x7e,0x08,0x08,0x08,0x08,0x00,0x00,0x00}, // 4
        {0x00,0x00,0x7e,0x40,0x40,0x40,0x7c,0x02,0x02,0x02,0x02,0x42,0x3c,0x00,0x00,0x00}, // 5
        {0x00,0x00,0x3c,0x40,0x40,0x40,0x7c,0x42,0x42,0x42,0x42,0x42,0x3c,0x00,0x00,0x00}, // 6
        {0x00,0x00,0x7e,0x02,0x02,0x04,0x08,0x10,0x20,0x20,0x20,0x20,0x20,0x00,0x00,0x00}, // 7
        {0x00,0x00,0x3c,0x42,0x42,0x42,0x3c,0x42,0x42,0x42,0x42,0x42,0x3c,0x00,0x00,0x00}, // 8
        {0x00,0x00,0x3c,0x42,0x42,0x42,0x42,0x42,0x3e,0x02,0x02,0x02,0x3c,0x00,0x00,0x00}  // 9
    };

    // 智能查询：如果是数字，直接返回；如果是特定字母，返回对应数组
    if (c >= '0' && c <= '9') return font_nums[c - '0'];
    switch (c) {
        case 'T': return font_T;
        case 'e': return font_e;
        case 'm': return font_m;
        case 'p': return font_p;
        case 'C': return font_C;
        case ':': return font_colon;
	case '.': return font_dot;
        case ' ': return font_space;
        default:  return font_space; // 不认识的字符统统画空格，防止越界崩溃
    }
}

// --- 2. 核心函数：绘制单个字符 ---
void drawChar(int *pixel_ptr, int xres, int x, int y, const unsigned char *data, int color) {
    for (int i = 0; i < 16; i++) {
        unsigned char line = data[i];
        for (int j = 0; j < 8; j++) {
            if ((line >> (7 - j)) & 0x1) {
                int target_index = (y + i) * xres + (x + j);
                pixel_ptr[target_index] = color;
            }else {
                int target_index = (y + i) * xres + (x + j);
		pixel_ptr[target_index] = 0x00000000; // 无笔画，强制画背景色 (如深灰)，彻底杜绝重影！
            }
        }
	// 【修补缝隙】强行把字符右侧那 2 个像素的间距也涂黑！
        pixel_ptr[(y + i) * xres + (x + 8)] = 0x00000000;
        pixel_ptr[(y + i) * xres + (x + 9)] = 0x00000000;

    }
}

// --- 3. 进阶函数：绘制整个字符串 ---
void drawString(int *pixel_ptr, int xres, int x, int y, const std::string& str, int color) {
    int current_x = x;
    for (char c : str) {
        const unsigned char* char_data = getFontData(c);
        drawChar(pixel_ptr, xres, current_x, y, char_data, color);
        current_x += 10; // 每个字符占 8 像素，加上 2 像素间距

    }
    // 【推土机擦除】在字符串屁股后面，强行画 3 个纯黑的空格！
    // 彻底干掉 "45CC" 或者 "46.5" 这种尾部残留垃圾！
    drawChar(pixel_ptr, xres, current_x, y, getFontData(' '), color);
    drawChar(pixel_ptr, xres, current_x + 10, y, getFontData(' '), color);
    drawChar(pixel_ptr, xres, current_x + 20, y, getFontData(' '), color);
}
